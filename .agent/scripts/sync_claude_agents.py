# Sync functions
# ---------------------------------------------------------------------------

_drift: list[str] = []  # populated in check mode


def _write_file(out_path: "Path", content: str, dry_run: bool, check: bool) -> None:
    """Write content to out_path, or in check/dry-run mode just report."""
    rel = out_path.relative_to(REPO_ROOT)
    if dry_run:
        print(f"  [DRY] {rel}")
        return
    if check:
        existing = out_path.read_text(encoding="utf-8") if out_path.exists() else ""
        if existing != content:
            print(f"  [DRIFT] {rel}")
            _drift.append(str(rel))
        else:
            print(f"  [OK]    {rel}")
        return
    out_path.write_text(content, encoding="utf-8")
    print(f"  ✓ {rel}")


def sync_agents(dry_run: bool = False, only_agent: str = "", profile: str = "", check: bool = False) -> None:
    CLAUDE_AGENTS_OUT.mkdir(parents=True, exist_ok=True)

    agent_files = sorted(AGENTS_SRC.glob("*.md"))
    if only_agent:
        agent_files = [f for f in agent_files if f.stem == only_agent]
        if not agent_files:
            print(f"ERROR: agent '{only_agent}' not found in {AGENTS_SRC}")
            sys.exit(1)

    if profile:
        filtered = []
        for f in agent_files:
            fm, _ = parse_frontmatter(f.read_text(encoding="utf-8"))
            if agent_matches_profile(fm, profile):
                filtered.append(f)
        skipped = len(agent_files) - len(filtered)
        agent_files = filtered
        print(f"\n=== Syncing specialist agents ({len(agent_files)}, profile={profile!r}, skipped={skipped}) ===")
    else:
        print(f"\n=== Syncing specialist agents ({len(agent_files)}) ===")

    for src in agent_files:
        out_path = CLAUDE_AGENTS_OUT / src.name
        content = build_claude_agent(src, is_workflow=False)
        _write_file(out_path, content, dry_run, check)


def sync_workflows(dry_run: bool = False, check: bool = False) -> None:
    CLAUDE_AGENTS_OUT.mkdir(parents=True, exist_ok=True)

    workflow_files = sorted(WORKFLOWS_SRC.glob("*.md"))
    print(f"\n=== Syncing workflow agents ({len(workflow_files)}) ===")
    for src in workflow_files:
        out_path = CLAUDE_AGENTS_OUT / f"wf-{src.name}"
        content = build_claude_agent(src, is_workflow=True)
        _write_file(out_path, content, dry_run, check)


def sync_commands(dry_run: bool = False, check: bool = False) -> None:
    """Generate .claude/commands/ from .agent/workflows/.

    Each workflow becomes a /slash-command in Claude Code.
    $ARGUMENTS is preserved verbatim — compatible with both Antigravity and Claude Code.
    .agent/workflows/ files are never modified (Antigravity backward compatibility).
    """
    CLAUDE_COMMANDS_OUT.mkdir(parents=True, exist_ok=True)

    workflow_files = sorted(WORKFLOWS_SRC.glob("*.md"))
    print(f"\n=== Syncing slash commands ({len(workflow_files)}) ===")

    unknown = [f.stem for f in workflow_files if f.stem not in COMMAND_META]
    if unknown:
        print(f"  WARNING: no COMMAND_META entry for: {unknown}")
        print(f"  → These will use default tools: {WORKFLOW_DEFAULT_TOOLS}")

    for src in workflow_files:
        out_path = CLAUDE_COMMANDS_OUT / src.name
        content = build_command(src)
        _write_file(out_path, content, dry_run, check)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="Sync .agent/ → .claude/agents/ and .claude/commands/")
    parser.add_argument("--dry-run", action="store_true", help="Print what would be written without writing")
    parser.add_argument("--check", action="store_true", help="Exit with code 1 if any file would change (CI mode)")
    parser.add_argument("--agent", metavar="NAME", help="Sync only one specialist agent by name")
    parser.add_argument(
        "--profile",
        metavar="NAME",
        help="Filter agents by distribution profile (go-service, web-app, data-platform, mobile, game). "
             "Universal agents (no profile: frontmatter) are always included.",
    )
    parser.add_argument("--no-workflows", action="store_true", help="Skip wf- workflow agents in .claude/agents/")
    parser.add_argument("--no-commands", action="store_true", help="Skip slash commands in .claude/commands/")
    parser.add_argument(
        "--patch-source-tools",
        action="store_true",
        help="One-time migration: write tools: into .agent/agents/*.md source files (Variant B)",
    )
    args = parser.parse_args()

    print(f"Repo root : {REPO_ROOT}")
    print(f"Agents out: {CLAUDE_AGENTS_OUT}")
    print(f"Commands  : {CLAUDE_COMMANDS_OUT}")

    if args.patch_source_tools:
        patch_source_tools(dry_run=args.dry_run)
        return

    check = args.check
    dry_run = args.dry_run

    sync_agents(dry_run=dry_run, only_agent=args.agent, profile=args.profile or "", check=check)

    if not args.agent:
        if not args.no_workflows:
            sync_workflows(dry_run=dry_run, check=check)
        if not args.no_commands:
            sync_commands(dry_run=dry_run, check=check)

    if check:
        if _drift:
            print(f"\nERROR: {len(_drift)} file(s) out of sync. Run 'make sync' to fix.")
            sys.exit(1)
        else:
            print("\nAll files in sync.")
    elif not dry_run:
        n_agents   = len(list(CLAUDE_AGENTS_OUT.glob("*.md")))
        n_commands = len(list(CLAUDE_COMMANDS_OUT.glob("*.md")))
        print(f"\nDone. agents: {n_agents}  commands: {n_commands}")


if __name__ == "__main__":
    main()
