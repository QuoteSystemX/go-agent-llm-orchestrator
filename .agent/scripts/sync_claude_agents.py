# Builders
# ---------------------------------------------------------------------------

def build_claude_agent(src_path: Path, is_workflow: bool = False) -> str:
    """Build .claude/agents/*.md content from a source .agent/agents/ or workflows/ file.

    Specialist agents:
      - `skills:` is stripped from output frontmatter (Antigravity-only field)
      - `tools:` is taken from source frontmatter or AGENT_DEFAULT_TOOLS
      - A pointer block "Read these skill files before starting" replaces inline embedding

    Workflow agents:
      - $ARGUMENTS replaced with a Claude Code note (subagents receive context differently)
      - tools: defaults to WORKFLOW_DEFAULT_TOOLS
    """
    raw = src_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(raw)

    agent_name = src_path.stem
    skills = parse_skills_list(fm.get("skills", ""))

    clean_fm: dict = {}
    clean_fm["name"] = fm.get("name", agent_name)

    desc = fm.get("description", "")
    if desc:
        clean_fm["description"] = desc

    # tools: source frontmatter wins; fall back to per-agent default or workflow default
    tools = (
        fm.get("tools")
        or (None if is_workflow else AGENT_DEFAULT_TOOLS.get(agent_name))
        or WORKFLOW_DEFAULT_TOOLS
    )
    clean_fm["tools"] = tools

    model = fm.get("model", "")
    if model and model != "inherit":
        clean_fm["model"] = model

    parts = [GENERATED_HEADER, render_frontmatter(clean_fm)]

    if is_workflow:
        body = body.replace(
            "$ARGUMENTS",
            "_Context from user message is passed automatically by Claude Code._",
        )

    parts.append(body)

    # Dynamic skill loading — pointer block instead of embedded content.
    # The agent reads skill files on demand via its Read tool.
    # Antigravity loads the same skills dynamically via `skills:` frontmatter.
    if skills and not is_workflow:
        missing = [s for s in skills if not skill_exists(s)]
        if missing:
            print(f"  WARNING [{agent_name}]: skill(s) not found: {missing}")

        skill_lines = "\n".join(
            f"- `.agent/skills/{s}/SKILL.md`" for s in skills
        )
        parts.append(
            "\n---\n\n"
            "> **Skills** — read these files with the `Read` tool before starting:\n"
            f"{skill_lines}\n"
        )

    return "\n".join(parts)


def build_command(src_path: Path) -> str:
    """Build .claude/commands/<name>.md content from a .agent/workflows/ file.

    Keeps $ARGUMENTS verbatim — both Antigravity and Claude Code commands use the same syntax.
    Adds Claude Code-specific frontmatter (argument-hint, allowed-tools) on top of the
    original description field, without touching any Antigravity workflow content.
    """
    raw = src_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(raw)

    name = src_path.stem
    meta = COMMAND_META.get(name, {})

    cmd_fm: dict = {}

    desc = fm.get("description", "")
    if desc:
        cmd_fm["description"] = desc

    hint = meta.get("argument-hint", "")
    if hint:
        cmd_fm["argument-hint"] = hint

    allowed = meta.get("allowed-tools", WORKFLOW_DEFAULT_TOOLS)
    cmd_fm["allowed-tools"] = allowed

    parts = [GENERATED_HEADER, render_frontmatter(cmd_fm)]
    parts.append(body)

    return "\n".join(parts)


# ---------------------------------------------------------------------------
# Migration helper: write tools: into source agent files (Variant B)
# ---------------------------------------------------------------------------

def patch_source_tools(dry_run: bool = False) -> None:
    """Write `tools:` field into .agent/agents/*.md source files.

    This is a one-time migration step for Variant B (dual-compatible frontmatter).
    After running this, source files are directly usable by Claude Code without
    transformation — symlinks become viable, and the sync script only needs to
    handle workflows and the skill pointer block.

    Safe to re-run: skips files that already have `tools:` in frontmatter.
    """
    print("\n=== Patching source agent files with tools: (Variant B migration) ===")
    patched = 0
    skipped = 0

    for src in sorted(AGENTS_SRC.glob("*.md")):
        raw = src.read_text(encoding="utf-8")
        fm, body = parse_frontmatter(raw)

        if fm.get("tools"):
            print(f"  SKIP  {src.name}  (already has tools:)")
            skipped += 1
            continue

        agent_name = src.stem
        tools = AGENT_DEFAULT_TOOLS.get(agent_name)
        if not tools:
            print(f"  SKIP  {src.name}  (no entry in AGENT_DEFAULT_TOOLS)")
            skipped += 1
            continue

        # Inject tools: after description: in frontmatter
        fm_ordered: dict = {}
        for key in ("name", "description", "tools", "skills", "model"):
            if key == "tools":
                fm_ordered["tools"] = tools
            elif key in fm:
                fm_ordered[key] = fm[key]
        for key, val in fm.items():
            if key not in fm_ordered:
                fm_ordered[key] = val

        new_content = render_frontmatter(fm_ordered) + "\n" + body
        if dry_run:
            print(f"  [DRY] {src.name}  → tools: {tools}")
        else:
            src.write_text(new_content, encoding="utf-8")
            print(f"  ✓ {src.name}  → tools: {tools}")
        patched += 1

    print(f"\nPatched: {patched}  Skipped: {skipped}")


