def cleanup_orphans(target: str, expected_files: set, dry_run: bool):
    config = TARGETS[target]
    for out_dir in [config["agents_out"], config["commands_out"]]:
        if not out_dir.exists():
            continue
        for item in out_dir.glob("*.md"):
            if item not in expected_files:
                rel = item.relative_to(REPO_ROOT)
                if dry_run:
                    print(f"  [ORPHAN-DRY] {rel}")
                else:
                    item.unlink()
                    print(f"  🗑️ {rel}")

def get_profile_extra_agents(profile_name: str) -> set:
    if not profile_name:
        return set()
    profile_path = REPO_ROOT / ".github" / "profiles.yml"
    if not profile_path.exists():
        return set()
    try:
        import yaml
        with open(profile_path) as f:
            data = yaml.safe_load(f)
            return set(data.get("profiles", {}).get(profile_name, {}).get("extra_agents", []))
    except Exception:
        return set()

def should_include_agent(src: Path, profile_name: str, extra_agents: set) -> bool:
    if not profile_name:
        return True
    if src.stem in extra_agents:
        return True
    
    # Check frontmatter
    content = src.read_text()
    if not content.startswith("---"):
        return True # Universal if no frontmatter
        
    parts = content.split("---", 2)
    if len(parts) < 3:
        return True
        
    try:
        import yaml
        fm = yaml.safe_load(parts[1])
        agent_profiles = fm.get("profile", "")
        if not agent_profiles:
            return True # Universal
        
        allowed = [p.strip() for p in str(agent_profiles).split(",")]
        return profile_name in allowed
    except Exception:
        return True # Fallback to include

def sync(target: str, dry_run: bool = False, check: bool = False, only_agent: str = "", profile: str = "", no_commands: bool = False, no_workflows: bool = False):
    config = TARGETS[target]
    config["agents_out"].mkdir(parents=True, exist_ok=True)
    config["commands_out"].mkdir(parents=True, exist_ok=True)

    print(f"\n=== Syncing TARGET: {target} ===")
    
    expected_files = set()
    extra_agents = get_profile_extra_agents(profile)

    # 1. Specialist Agents
    agent_files = sorted(AGENTS_SRC.rglob("**/*.md"))
    if only_agent:
        agent_files = [f for f in agent_files if f.stem == only_agent]
    
    for src in agent_files:
        if not should_include_agent(src, profile, extra_agents):
            continue
            
        out_path = config["agents_out"] / src.name
        expected_files.add(out_path)
        content = build_agent_file(src, target, is_workflow=False)
        _write(out_path, content, dry_run, check)

    # 2. Workflows as Agents (Claude only usually)
    if target == "claude" and not no_workflows:
        for src in sorted(WORKFLOWS_SRC.glob("*.md")):
            # Workflows are generally universal unless we add profile support for them too
            out_path = config["agents_out"] / f"wf-{src.name}"
            expected_files.add(out_path)
            content = build_agent_file(src, target, is_workflow=True)
            _write(out_path, content, dry_run, check)

    # 3. Workflows as Commands
    if not no_commands:
        for src in sorted(WORKFLOWS_SRC.glob("*.md")):
            out_path = config["commands_out"] / src.name
            expected_files.add(out_path)
            content = build_command_file(src, target)
            _write(out_path, content, dry_run, check)

    # 4. Cleanup
    if not only_agent and not profile:
        cleanup_orphans(target, expected_files, dry_run)

def _write(path: Path, content: str, dry_run: bool, check: bool):
    rel = path.relative_to(REPO_ROOT)
    if dry_run:
        print(f"  [DRY] {rel}")
        return
    if check:
        if not path.exists():
            print(f"  [MISSING] {rel}")
            sys.exit(1)
        existing = path.read_text()
        if existing != content:
            print(f"  [DRIFT] {rel}")
            sys.exit(1)
        return
    path.write_text(content)
    print(f"  ✓ {rel}")

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--target", choices=["claude", "opencode"], default="claude")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--check", action="store_true")
    parser.add_argument("--agent", metavar="NAME", default="")
    parser.add_argument("--profile", metavar="NAME", default="")
    parser.add_argument("--no-commands", action="store_true")
    parser.add_argument("--no-workflows", action="store_true")
    args = parser.parse_args()

    sync(args.target, args.dry_run, args.check, args.agent, args.profile, args.no_commands, args.no_workflows)

if __name__ == "__main__":
    main()
