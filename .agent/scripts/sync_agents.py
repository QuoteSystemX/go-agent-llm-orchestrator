def build_agent_file(src_path: Path, target: str, is_workflow: bool = False) -> str:
    raw = src_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(raw)
    agent_name = src_path.stem
    skills = [s.strip() for s in fm.get("skills", "").split(",") if s.strip()]

    clean_fm: dict = {}
    clean_fm["name"] = fm.get("name", agent_name)
    if fm.get("description"): clean_fm["description"] = fm["description"]

    # Target specific logic
    if target == "claude":
        clean_fm["tools"] = fm.get("tools") or AGENT_DEFAULT_TOOLS.get(agent_name) or WORKFLOW_DEFAULT_TOOLS
    elif target == "opencode":
        # OpenCode might use standard frontmatter
        if fm.get("model"): clean_fm["model"] = fm["model"]

    parts = [TARGETS[target]["header"], render_frontmatter(clean_fm)]
    
    if is_workflow and target == "claude":
        body = body.replace("$ARGUMENTS", "_Context from user message is passed automatically by Claude Code._")
    
    parts.append(body)

    if skills and target == "claude" and not is_workflow:
        skill_lines = "\n".join(f"- `.agent/skills/{s}/SKILL.md`" for s in skills if skill_exists(s))
        if skill_lines:
            parts.append("\n---\n\n> **Skills** — read these files with the `Read` tool before starting:\n" + skill_lines + "\n")

    return "\n".join(parts)

def build_command_file(src_path: Path, target: str) -> str:
    raw = src_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(raw)
    name = src_path.stem
    meta = COMMAND_META.get(name, {})

    cmd_fm: dict = {}
    if fm.get("description"): cmd_fm["description"] = fm["description"]

    if target == "claude":
        cmd_fm["argument-hint"] = meta.get("argument-hint", "")
        cmd_fm["allowed-tools"] = meta.get("allowed-tools", WORKFLOW_DEFAULT_TOOLS)
    elif target == "opencode":
        # OpenCode commands often specify an agent or mode
        cmd_fm["agent"] = fm.get("agent", "orchestrator")
        if meta.get("argument-hint"):
            body = f"> Hint: {meta['argument-hint']}\n\n" + body

    parts = [TARGETS[target]["header"], render_frontmatter(cmd_fm), body]
    return "\n".join(parts)

